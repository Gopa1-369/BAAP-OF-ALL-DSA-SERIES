#include <iostream>
using namespace std;

// Write a program that takes two integers as input and prints their sum.

int main()
{
    int a;
    int b;

    cout << "Enter the first integer number : " << endl;
    cin >> a;

    cout << "Enter the second integer number : " << endl;
    cin >> b;

    cout << "the sum of a + b is :" << a + b;
}
