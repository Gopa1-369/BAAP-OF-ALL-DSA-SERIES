#include<iostream>
using namespace std;

int main(){
     int x=5;
     //this operator first increase the x varible value by 1 thne print it
     // so here 5+1 =6    x=6
     cout<<++x;    //preincrement operator

     //here first print x and after increment the value of x
     // so previous x=6 so x print and after  x value increase by 1 
     //  6+1=7   x=7
     cout<<x++;     //postincrement operator
    
}